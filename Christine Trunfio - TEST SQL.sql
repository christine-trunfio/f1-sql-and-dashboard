select ds.points, d.name, t.name as 'Team'
from drivers d
inner join drivers_stands ds on d.id = ds.id_driver
inner join teams t on t.id = ds.id_driver_team
inner join seasons s on s.year = ds.id_season
where s.year = 2022
order by ds.points desc;

-- 9. Visualizzare il calendario della stagione 2023 con Data del GP, 
-- il nome del GP (Gran Premio di [nome paese]), luogo del GP ([nome citta, nome paese])
-- e il Circuito [nome circuito]

select r.date_race, concat('Gran Premio di ', c.country) as 'Evento', concat(c.city, ', ', c.country) as 'Luogo', c.name as 'Circuito'
from races r
inner join circuits c on c.id = r.id_circuit
inner join seasons s on r.id_season = s.year
where s.year = 2023
order by r.date_race asc;

-- 10. Visualizzare della stagione 2022 il nome del GP, l'anno, il mese, il pilota che
-- ha vinto, il pilota partito in pole position e il pilota che ha fatto il giro più veloce

select concat('Gran Premio di ', c.country) as 'Evento', year(r.date_race) as 'Anno', 
	   monthname(r.date_race) as 'Mese', d1.name as 'Vincitore', 
       d2.name as 'Pole Position', d3.name as 'Giro Veloce'
from races r
inner join circuits c on c.id = r.id_circuit
inner join drivers d1 on r.id_driver_win = d1.id
inner join drivers d2 on r.id_driver_pole = d2.id
inner join drivers d3 on r.id_driver_fastlap = d3.id
inner join seasons s on r.id_season = s.year
where s.year = 2022
order by r.date_race asc;

