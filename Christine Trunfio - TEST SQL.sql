-- drop database formula1;
create database formula1;
use formula1;

create table circuits (
	id int primary key auto_increment,
    name varchar(45),
    city varchar(45),
    country varchar(45),
    laps int,
    length int,
    wr_time varchar(10));
    
create table drivers (
	id int primary key auto_increment,
    name varchar (45),
    board_name varchar(45),
    number varchar(2),
    country varchar(45),
    date_born date);
    
create table teams(
	id int primary key auto_increment,
    name varchar(45),
    country varchar(45),
    engine varchar(45));
    
create table seasons(
	year int primary key);
    
create table races(
	id int primary key auto_increment,
    id_season int,
    id_circuit int,
    date_race date,
    id_driver_win int,  --
    id_driver_pole int, -- fanno tutti e tre riferimento all'ID del driver
    id_driver_fastlap int,
    foreign key(id_season) references seasons(year)
    on update cascade on delete cascade,
	foreign key(id_circuit) references circuits(id)
    on update cascade on delete cascade,
    foreign key(id_driver_win) references drivers(id)
    on update cascade on delete cascade,
    foreign key(id_driver_pole) references drivers(id)
    on update cascade on delete cascade,
    foreign key(id_driver_fastlap) references drivers(id)
    on update cascade on delete cascade);
    
create table race_details (
	id int primary key auto_increment,
    id_race int,
    id_driver int,
    id_driver_team int,
    start_pos varchar(3),
    end_pos varchar(3),
    foreign key(id_driver) references drivers(id)
    on update cascade on delete cascade,
    foreign key(id_race) references races(id)
    on update cascade on delete cascade,
    foreign key(id_driver_team) references teams(id)
    on update cascade on delete cascade);
    
create table teams_stands(
	id int primary key auto_increment,
    id_season int,
    id_team int,
    points int,
    foreign key(id_season) references seasons(year)
    on update cascade on delete cascade,
    foreign key(id_team) references teams(id)
    on update cascade on delete cascade);
    
create table drivers_stands(
	id int primary key auto_increment,
    id_season int,
    id_driver int,
    id_driver_team int,
    points int,
    foreign key(id_season) references seasons(year)
    on update cascade on delete cascade,
    foreign key(id_driver) references drivers(id)
    on update cascade on delete cascade,
    foreign key(id_driver_team) references teams(id)
    on update cascade on delete cascade);
    
    
