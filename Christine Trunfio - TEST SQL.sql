-- 3. Visualizzare il numero di team che usano lo stesso motore

select count(name) as 'Numero di team', engine
from teams
group by engine;

-- 4. Visualizzare il circuito con il numero maggiore di giri e il circuito con il numero minore di giri

select name, laps
from circuits
where laps = (select max(laps) from circuits) or laps = (select min(laps) from circuits);

-- 5. Visualizzare i circuiti con il record per giro più alto e quello più basso

select name as 'Nome circuito', wr_time as 'Record'
from circuits
where wr_time = (select max(wr_time) from circuits) or wr_time = (select min(wr_time) from circuits);

-- 6. Visualizzare i punti totali di ogni team di ogni stagione

select t.name as 'Team', s.year as 'Stagione', sum(points) as 'Punti'
from teams t
inner join teams_stands ts on t.id = ts.id_team
inner join seasons s on s.year = ts.id_season
group by t.name, s.year
order by t.name;

-- 7. Visualizzare il numero di gare che si svolgono in ogni mese (nome del mese)

select count(id) as 'Numero di gare', monthname(date_race) as 'Mese'
from races
group by monthname(date_race);

