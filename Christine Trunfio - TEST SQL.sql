-- 14. Visualizzare il numero di volte in cui un pilota non ha finito la gara

select d.name, count(rd.end_pos) as 'Ritiri'
from race_details rd
inner join drivers d on rd.id_driver = d.id
where rd.end_pos = 'OUT'
group by d.name
order by count(rd.end_pos) desc;

-- 15. Visualizzare per ogni gara il PODIO (posizione di arrivo 1-2-3)

select r.date_race, concat(rd1.end_pos, ': ', d1.name) as 'Primo', concat(rd2.end_pos, ': ', d2.name) as 'Secondo', concat(rd3.end_pos, ': ', d3.name) as 'Terzo'
from races r
inner join race_details rd1 on rd1.id_race = r.id
inner join drivers d1 on rd1.id_driver = d1.id
inner join race_details rd2 on rd2.id_race = r.id
inner join drivers d2 on rd2.id_driver = d2.id
inner join race_details rd3 on rd3.id_race = r.id
inner join drivers d3 on rd3.id_driver = d3.id
where rd1.end_pos = '1' and rd2.end_pos = '2' and rd3.end_pos = '3'
order by r.date_race asc;
