select r.date_race as 'Data',  concat('Gran Premio di ', c.country) as 'Evento', count(rd.end_pos) as 'Ritiri'
from race_details rd
inner join races r on rd.id_race = r.id
inner join circuits c on c.id = r.id_circuit
where rd.end_pos = 'OUT'
group by r.date_race, c.country
order by r.date_race;

-- 13. Visualizzare i piloti con più partenze dal pit lane e quelli con meno

select d.name, count(rd.start_pos) as pit_lanes
from drivers d
inner join race_details rd on rd.id_driver = d.id
where rd.start_pos = 'PL'
group by d.name
having count(rd.start_pos) = (select max(pit_lanes) from 
(select count(rd2.start_pos) as pit_lanes from race_details rd2 where rd2.start_pos = 'PL' group by rd2.id_driver) as max_pits)
or count(rd.start_pos) = (select min(pit_lanes) from
(select count(rd3.start_pos) as pit_lanes from race_details rd3 where rd3.start_pos = 'PL' group by rd3.id_driver) as min_pits)
order by pit_lanes desc;

